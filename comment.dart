void main() {
  // This is a single-line comment
  // It explains the next line of code

  int number = 10; // Declare an integer variable

  print("The number is $number"); // Print the number

  /*
    This is a multi-line comment.
    It can span across multiple lines.
    You can use it to explain blocks of code in detail.
  */

  int square = number * number; // Calculate the square
  print("The square of $number is $square");

  /*
    You can also use multi-line comments
    to temporarily disable blocks of code like this:

    print("This won't run");
    int x = 5 + 5;
  */
}
