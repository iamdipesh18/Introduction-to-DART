void main() {
  //addition
  num a = 1;
  num b = 2;
  num c = a + b;
  print("$a + $b = $c");
  //subtraction
  num d = a - b;
  print("$a - $b = $d");
  //unary minus
  num e = -d;
  print(e);
  //multiplication
  num f = a * b;
  print("$a * $b = $f");
  //division
  num g = a / b;
  print("$a / $b = $g");
  //integer division
  num h = a ~/ b;
  print("$a ~/ $b = $h");
  //modulus
  num i = a % b;
  print("$a mod $b = $i");
}
